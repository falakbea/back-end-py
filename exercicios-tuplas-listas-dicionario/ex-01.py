# Utilizando listas faça um programa que faça 5 perguntas para uma pessoa sobre um crime.
# As perguntas são:
# ""Telefonou para a vítima?""
# ""Esteve no local do crime?""
# ""Mora perto da vítima?""
# ""Devia para a vítima?""
# ""Já trabalhou com a vítima?""

lista_perguntas = []
pergunta = input('Telefonou para a vítima? ')
pergunta1 = input('Esteve no local do crime?? ')
pergunta2 = input('Mora perto da vítima? ')
pergunta3 = input('Devia para a vítima? ')
pergunta4 = input('Já trabalhou com a vítima? ')
lista_perguntas.append(pergunta)
lista_perguntas.append(pergunta1)
lista_perguntas.append(pergunta2)
lista_perguntas.append(pergunta3)
lista_perguntas.append(pergunta4)
print(lista_perguntas)